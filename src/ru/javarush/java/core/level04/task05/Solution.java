package ru.javarush.java.core.level04.task05;

public class Solution {
    public static void main(String[] args) {
        // Цикл for для обратного отсчёта от 5 до 1
        for (int i = 5; i >= 1; i--) {
            // Вывод текущего числа на новой строке
            System.out.println(i);
        }
        // Вывод поздравления после завершения цикла
        System.out.println("С Новым годом!");
    }
}